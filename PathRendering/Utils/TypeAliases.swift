public typealias MetalFloat3x4 = simd_float3x4

public typealias MetalFloat4 = SIMD4<MetalFloat>
public typealias MetalFloat4x2 = simd_float4x2
public typealias MetalFloat4x3 = simd_float4x3
public typealias MetalFloat4x4 = simd_float4x4

public typealias MetalColor = SIMD4<Float>
